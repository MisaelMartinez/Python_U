"""
Los operadores condicionales se utiliazn para comparar dos valores

== Igual
!= Distinto
< Mayor
> Menor
<= Mayor o igual
>= Menor o igual
"""

a = 10
b = 15

print(f'El resultado es igual? {a == b}')
print(f'El resultado es diferente? {a != b}')
print(f'\'a\' es mayor que \'b\'? {a < b}')
print(f'\'a\' es menor que \'b\'? {a > b}')
print(f'\'a\' es mayor o igual que \'b\'? {a <= b}')
print(f'\'a\' es menor o igual que \'b\'? {a >= b}')



