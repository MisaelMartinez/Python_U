"""
Para obtener la longitud de una cadena utilizamos la fncion incorporada len()

La funcion len funcoona en varios tipos de daos incluyendo cadenas, listas, etc.
Cuando se calcula el largo de una cadenas se toma en cuenta todos los caracteres
de una cadena incluyendo espacios en blanco, caracteres especiales, etc
"""

c1 = ("Largo de una cadena")
longitud_cadena = len(c1)

print(f'El largo de \\{c1}\\ es: {longitud_cadena}')
#La cadena empieza con indice 0 a 18, len nos indica que hay 19 caracteres.







