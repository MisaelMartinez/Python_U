#Un operador puede comportarse de diferentes formas
# ' + ' Puede trabajar como: suma de enteros, string, o lista.

a = 2
b = 3
print(a+b)

a = 'Hola'
b = ' Mundo'
print(a+b)

a = [1,2,3]
b = [4,5,6]
print(a+b)

