"""
El ciclo for itera o recorre una secuencia de valores, por ejemplo, los caracteres de una cadena, una lista, etc
Y ejecuta un bloque de codigo por cada elemento de la secuencia.
"""

x = 'Hola Mundo'
for i in x:
    #print(i) #Se imprime una letra en cada renglon.
    print(i, end = '')#Imprime todo en un renglon, uno por uno
print('\n')

frutas = ['Platano', 'Mango', 'Fresa']
for i in frutas:
    print(i)









