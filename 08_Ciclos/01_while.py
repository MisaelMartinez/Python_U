"""
Los ciclos en python son estructuras de control que repiten una serie de instrucciones hasta que se cumple
una condicion especifica.

El ciclo WHILE repite una serie de instrucciones mientras la condicion a evaluar sea verdadera.

"""

i=1
while i <=3:
    print(f'Esta es la cuenta: {i}', end = '\n') #Por ahora el \n esta de sobra. Puede ser un s ' ' o '-'
    i +=1

print(f'se salio del while')

